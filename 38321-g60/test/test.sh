sed -E '
# 匹配表格前三行
1,3 {
    # 将行存入保持空间（Hold Space）
    H
    # 当处理到第三行时执行合并
    3 {
        x  # 交换模式空间和保持空间
        # 合并三行内容（忽略中间空行）
        s/^([^\n]*)\n[^\n]*\n([^\n]*)\n$/\1 \2/
        # 格式化输出（保持列结构）
        s/ \| /|/g
        s/ +/ /g
        # 输出合并后的行
        p
        # 添加原分隔线
        a +===================+========+======+
        # 删除模式空间（避免重复输出）
        d
    }
}
' input.md > output.md